package bean;

public class Thumbsup {
	int opinion_id;
	String e_mail;
	int sub_id;
	int up_id;
	public Thumbsup(){
		
	}
	public int getOpinion_id() {
		return opinion_id;
	}
	public void setOpinion_id(int opinion_id) {
		this.opinion_id = opinion_id;
	}
	public String getE_mail() {
		return e_mail;
	}
	public void setE_mail(String e_mail) {
		this.e_mail = e_mail;
	}
	public int getSub_id() {
		return sub_id;
	}
	public void setSub_id(int sub_id) {
		this.sub_id = sub_id;
	}
	public int getUp_id() {
		return up_id;
	}
	public void setUp_id(int up_id) {
		this.up_id = up_id;
	}
}
